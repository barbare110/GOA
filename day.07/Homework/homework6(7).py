# Control Flow Techniques

# 1. Sequencing
# Sequencing is the fundamental control flow technique in programming.
# It refers to the execution of statements in the exact order they are written.
# Each line of code runs one after another, ensuring that actions are performed in a linear fashion.
# This is crucial for tasks that require a specific order, such as initializing variables before using them.

# 2. Selection
# Selection control flow allows the program to choose different paths of execution based on certain conditions.
# This is implemented using conditional statements like 'if', 'elif', and 'else'.
# By evaluating conditions, the program can decide which block of code to execute.
# This technique is vital for decision-making processes, enabling the program to respond dynamically to varying inputs or states.

# 3. Iteration
# Iteration is the control flow technique that facilitates the repeated execution of a block of code.
# It enables a program to perform tasks multiple times until a specified condition is met.
# This can be achieved using loops such as 'for' and 'while'.
# Iteration is particularly useful for tasks involving collections, repetitive calculations
#  or any scenario where the number of required repetitions is not known in advance.