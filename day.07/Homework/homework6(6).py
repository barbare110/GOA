# Different Techniques for Representing Algorithms

# 1. Step-by-Step
# This technique lists each step of the algorithm sequentially.
# Example:
# Step 1: Gather ingredients.
# Step 2: Mix ingredients in a bowl.
# Step 3: Baked for a specified time.

# 2. Flowchart
# A visual representation using shapes and arrows to show the flow of the algorithm.
# Example:
# Start (oval) -> Gather ingredients (rectangle) -> Mix ingredients (rectangle) -> Bake (rectangle) -> End (oval)
# Decisions can be shown with diamonds, like checking if the cake is baked. So its easy

# 3. Pseudocode
# A code-like representation that outlines the algorithm in a simple way, without specific programming syntax.
# Example:

# IF age >= 18 THEN
#     PRINT "You are an adult."
# ELSE
#    PRINT "You are a minor."