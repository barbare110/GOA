# An algorithm is a step-by-step procedure or formula for solving a problem.
# It consists of a series of instructions that need to be followed to achieve a specific outcome.

# Real-life examples:

# 1. Recipe for Cooking:
# Step 1: Gather ingredients (e.g., flour, sugar, eggs).
# Step 2: Mix ingredients in a bowl.
# Step 3: Bake in the oven at a specified temperature for a certain time.
# Step 4: Let it cool before serving.

# 2. Directions for Driving:
# Step 1: Start from your current location.
# Step 2: Turn left onto Main Street.
# Step 3: Drive for 2 miles.
# Step 4: Turn right onto "idk any street".
# Step 5: Your destination will be on the left.

# 3. Morning Routine:
# Step 1: Wake up at a specific time.
# Step 2: Brush your teeth.
# Step 3: Take a shower.
# Step 4: Get dressed.
# Step 5: Have breakfast.

# These examples illustrate how algorithms can be found in everyday tasks, guiding us through a series of steps to reach a desired outcome.